/*
* 正则：是一个用来处理字符串的规则
*   1. 正则只能用来处理字符串
*   2. 处理一般包含两方面：
*     2.1 验证当前的字符串是否符合某个规则【正则匹配】
*     2.2 把符合当前规则的字符获取到 【正则捕获】
*
*  学习正则就是学习如何编写正则，每一个正则都是由“元字符”和“修饰符”两部分组成的
*
*
* */

